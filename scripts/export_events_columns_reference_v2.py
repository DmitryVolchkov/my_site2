def main() -> None:
    wb = Workbook()
    ws = wb.active
    ws.title = "Справочник колонок"

    headers = ["поле", "русское наименование", "зачем"]
    header_fill = PatternFill("solid", fgColor="1A2A3A")
    thin = Side(style="thin", color="D4CFC4")
    border = Border(left=thin, right=thin, top=thin, bottom=thin)

    for col, value in enumerate(headers, 1):
        cell = ws.cell(row=1, column=col, value=value)
        cell.font = Font(bold=True, color="FFFFFF")
        cell.fill = header_fill
        cell.alignment = Alignment(horizontal="center", vertical="center", wrap_text=True)
        cell.border = border

    for row_idx, row in enumerate(ROWS, 2):
        for col_idx, value in enumerate(row, 1):
            cell = ws.cell(row=row_idx, column=col_idx, value=value)
            cell.alignment = Alignment(vertical="top", wrap_text=True)
            cell.border = border

    ws.freeze_panes = "A2"
    ws.auto_filter.ref = f"A1:C{len(ROWS) + 1}"
    ws.column_dimensions["A"].width = 28
    ws.column_dimensions["B"].width = 34
    ws.column_dimensions["C"].width = 82

    for row_idx in range(2, len(ROWS) + 2):
        ws.row_dimensions[row_idx].height = 36

    OUT.parent.mkdir(parents=True, exist_ok=True)
    wb.save(OUT)
    print("OK:", OUT)


if __name__ == "__main__":
    main()
